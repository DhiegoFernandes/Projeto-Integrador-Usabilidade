    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        Mesa mesa = new Mesa();

        try {
            int numeroMesa;

            //validando campos
            if (txtNumeroMesa.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Preencha o campo número da mesa!");
                txtNumeroMesa.requestFocus();
            } else {

                numeroMesa = Integer.parseInt(txtNumeroMesa.getText());

                mesa.setIdMesa(numeroMesa);

                MesasDAO mDAO = new MesasDAO();

                mDAO.InsereMesa(mesa);
                JOptionPane.showMessageDialog(null, "Mesa criada com sucesso!");

                Limpar();
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        try {

            if (txtExcluirID.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Preencha o campo excluir!");
                txtExcluirID.requestFocus();
            } else {
                int id;
                id = Integer.parseInt(txtExcluirID.getText());

                MesasDAO mDAO = new MesasDAO();

                //fazendo a chamada do metodo para excluir pedido pelo seu ID
                mDAO.deletarPorId(id);

                //Mensagem de retorno ao usuario
                JOptionPane.showMessageDialog(null, "Mesa excluida com sucesso!");
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }//GEN-LAST:event_btnExcluirActionPerformed


