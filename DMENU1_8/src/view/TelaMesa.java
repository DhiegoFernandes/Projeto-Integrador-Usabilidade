    public void LerParaTabelaPorMesa() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblListaMesas.getModel();
        model.setNumRows(0);

        MesasDAO mesas = new MesasDAO();

//Gera a lista de pizzas localmente, que sera preenchida
        List<Mesa> listaDeMesas;
        listaDeMesas = new ArrayList<>();

//pega (Obtem) a lista de pizzas atraves do atendente
        listaDeMesas = mesas.ListaTodasAsMesas();
        //for (Pedido pz : pDAO.buscaPorPedido(numPedido)) {
        for (Mesa mesaLocal : listaDeMesas) {

            model.addRow(new Object[]{
                mesaLocal.getIdMesa(),});
        }

    }

