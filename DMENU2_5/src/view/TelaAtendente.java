    private void btnCriaPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriaPedidoActionPerformed
        MesasDAO mDAO = new MesasDAO();

        try {
            
            int mesa = Integer.parseInt(JOptionPane.showInputDialog(null, "Digite a mesa: ", "Digite a mesa", JOptionPane.INFORMATION_MESSAGE));
            
            if (mDAO.checkMesa(mesa)) {//verifica se a mesa existe

                JOptionPane.showMessageDialog(null, String.format("Bem-Vindo!: [Mesa: %d]",
                        mesa), "Bem-Vindo!", JOptionPane.INFORMATION_MESSAGE);

                new CriaPedidoCliente(mesa).setVisible(true);//Atribui o valor da mesa selecionado             

            } else {
                JOptionPane.showMessageDialog(null, "Mesa invalida! ", "Mesa n√£o definida!", JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(TelaAtendente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException ae) {//exececao quando clica em cancelar, n√£o est√° sendo tratada
            System.out.println("Entrada de dado da mesa cancelada. \n" + ae);

        }

    }//GEN-LAST:event_btnCriaPedidoActionPerformed

