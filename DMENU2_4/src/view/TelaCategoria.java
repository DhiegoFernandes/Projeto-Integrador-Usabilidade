    public void LerParaTabelaPorCategoria() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tblListaMesas.getModel();
        model.setNumRows(0);

        CategoriaDAO cDAO = new CategoriaDAO();

//Gera a lista de mesa localmente, que sera preenchida
        List<Categoria> listaDeCategorias;
        listaDeCategorias = new ArrayList<>();

//pega (Obtem) a lista de pizzas atraves da mesa
        listaDeCategorias = cDAO.listaTodasAsCategorias();

        for (Categoria mesaLocal : listaDeCategorias) {

            model.addRow(new Object[]{
                mesaLocal.getIdcategoria(),
                mesaLocal.getNome(),});
        }

    }

