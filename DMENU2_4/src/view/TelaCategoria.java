    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        Categoria cat = new Categoria();

        try {
            String nomeCategoria;

            //validando campos
            if (txtNomeCategoria.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Preencha o campo da categoria!");
                txtNomeCategoria.requestFocus();
            } else {

                nomeCategoria = txtNomeCategoria.getText().toLowerCase();//PEGA A CATEGORIA EM MINUSCULO

                cat.setNome(nomeCategoria);//Seta a categoria

                CategoriaDAO cDAO = new CategoriaDAO();

                cDAO.insereCategoria(cat);
                JOptionPane.showMessageDialog(null, "Categoria criada com sucesso!");

            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        try {

            if (txtExcluir.getText().length() < 1) {
                JOptionPane.showMessageDialog(null, "Preencha o campo excluir!");
                txtExcluir.requestFocus();
            } else {
                int id;
                id = Integer.parseInt(txtExcluir.getText());

                CategoriaDAO cDAO = new CategoriaDAO();

                cDAO.deletarPorId(id);

                //Mensagem de retorno ao usuario
                JOptionPane.showMessageDialog(null, "Categoria excluida com sucesso!");
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }//GEN-LAST:event_btnExcluirActionPerformed


